import os
import json
import matplotlib.pyplot as plt
import numpy as np

# --- Configuration ---
# Must match the directory in the runner script
results_dir = "./fedgd_clients20_path"
epsilons = [0]
rounds_list = [1, 2, 5, 10, 20, 30, 40, 50, 60, 70]

# Containers
all_accuracies = {}
all_losses = {}

print(f"Reading results from: {results_dir}")

for eps in epsilons:
    accuracies, losses = [], []

    for rounds in rounds_list:
        path = os.path.join(results_dir, f"fedgd_eps{eps}_rounds{rounds}.json")

        if not os.path.exists(path):
            print(f"  Missing file: {path}")
            accuracies.append(None)
            losses.append(None)
            continue

        try:
            with open(path, "r") as f:
                data = json.load(f)

            # Extract metrics (handle potential missing keys)
            final_stats = data.get("final_stats", {})
            acc = final_stats.get("accuracy", None)
            loss = final_stats.get("loss", None)

            if acc is None:
                print(f"  Warning: No accuracy found in {path}")

            accuracies.append(acc)
            losses.append(loss)

        except json.JSONDecodeError:
            print(f"  Error: Could not decode JSON {path}")
            accuracies.append(None)
            losses.append(None)

    all_accuracies[eps] = accuracies
    all_losses[eps] = losses

# --- Plotting Helper ---
def plot_metric(metric_dict, metric_name, filename):
    plt.figure(figsize=(8, 6))

    # Color map for distinct lines
    colors = plt.cm.viridis(np.linspace(0, 1, len(metric_dict)))

    for (eps, values), color in zip(metric_dict.items(), colors):
        # Filter out Nones for plotting
        valid_rounds = [r for r, v in zip(rounds_list, values) if v is not None]
        valid_values = [v for v in values if v is not None]

        if valid_values:
            plt.plot(valid_rounds, valid_values, marker="o", linestyle='-',
                     linewidth=2, markersize=6, label=f"ε = {eps}", color=color)
            for x, y in zip(valid_rounds, valid_values):
                # Formatting: 4 decimal places for accuracy, 2 for loss
                label_text = f"{y:.4f}" if metric_name == "Accuracy" else f"{y:.2f}"

                # Use annotate instead of text to support offsets
                plt.annotate(
                    label_text,
                    xy=(x, y),         # The point to annotate
                    fontsize=9,
                    fontweight='bold',
                    ha='center',       # horizontal alignment
                    va='bottom',       # vertical alignment
                    xytext=(0, 10),    # 10 point vertical offset
                    textcoords='offset points'
                )

    plt.title(f"DP-fedgd CIFAR-10 (ResNet20): {metric_name} vs Rounds")
    plt.xlabel("Federated Rounds")
    plt.ylabel(f"Test {metric_name}")
    plt.xticks(rounds_list)
    plt.grid(True, linestyle="--", alpha=0.4)
    plt.legend(title="Privacy Budget")
    plt.tight_layout()

    # Save and Show
    plt.savefig(filename, dpi=300)
    print(f"Saved plot to {filename}")
    plt.show()

# Generate Plots

plot_metric(all_accuracies, "Accuracy", "dp_sofim_accuracy_curve.png")


plot_metric(all_losses, "Loss", "dp_sofim_loss_curve.png")

