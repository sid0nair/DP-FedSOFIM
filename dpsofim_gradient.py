import os
import json
import glob

def diagnose_results_files():
    """
    Diagnostic script to see exactly what files exist and what data they contain.
    """

    alg_dirs = {
        "fedgd": "./results_sweep_fedgd_clients100",
        "sofim": "./results_sweep_sofim_clients100"
    }

    print("="*80)
    print("DIAGNOSTIC: Checking all result files")
    print("="*80)

    for alg, results_dir in alg_dirs.items():
        print(f"\n{'='*80}")
        print(f"Algorithm: {alg.upper()}")
        print(f"Directory: {results_dir}")
        print(f"{'='*80}")

        if not os.path.exists(results_dir):
            print(f"❌ DIRECTORY NOT FOUND: {results_dir}")
            continue

        pattern = os.path.join(results_dir, "*.json")
        files = sorted(glob.glob(pattern))

        print(f"\nFound {len(files)} JSON files\n")

        for i, filepath in enumerate(files, 1):
            filename = os.path.basename(filepath)
            print(f"\n{'-'*80}")
            print(f"File {i}: {filename}")
            print(f"{'-'*80}")

            try:
                with open(filepath, "r") as f:
                    data = json.load(f)

                # Extract config info
                config = data.get("config", {})
                sweep_cfg = data.get("sweep_config", {})

                epsilon = sweep_cfg.get("epsilon") or config.get("epsilon")
                rounds = sweep_cfg.get("rounds") or config.get("federated_rounds")

                print(f"  Config epsilon: {epsilon}")
                print(f"  Config rounds: {rounds}")

                # Extract round results
                rr = data.get("round_results", []) or data.get("results", {}).get("round_results", [])

                if not rr:
                    print(f"  ⚠️  NO ROUND RESULTS FOUND")
                    continue

                print(f"  Total round entries: {len(rr)}")

                # Check what rounds have data
                rounds_with_data = []
                for r in rr:
                    round_num = r.get("round")
                    if round_num is not None:
                        rounds_with_data.append(round_num)

                if rounds_with_data:
                    print(f"  Rounds with data: {sorted(rounds_with_data)}")
                    print(f"  First round: {min(rounds_with_data)}")
                    print(f"  Last round: {max(rounds_with_data)}")
                else:
                    print(f"  ⚠️  No 'round' field found in round_results")

                # Check for accuracy keys
                acc_keys_found = set()
                for r in rr:
                    for key in ["accuracy", "test_accuracy", "acc", "test_acc"]:
                        if key in r and r[key] is not None:
                            acc_keys_found.add(key)

                if acc_keys_found:
                    print(f"  Accuracy keys found: {acc_keys_found}")

                    # Show first 5 rounds with accuracy
                    print(f"\n  First 5 rounds with accuracy:")
                    count = 0
                    for r in rr:
                        round_num = r.get("round")
                        for key in acc_keys_found:
                            acc = r.get(key)
                            if round_num is not None and acc is not None:
                                print(f"    Round {round_num}: {key}={acc:.4f}")
                                count += 1
                                break
                        if count >= 5:
                            break
                else:
                    print(f"  ❌ NO ACCURACY DATA FOUND")
                    print(f"  Available keys in round_results[0]: {list(rr[0].keys()) if rr else 'N/A'}")

            except json.JSONDecodeError as e:
                print(f"  ❌ JSON DECODE ERROR: {e}")
            except Exception as e:
                print(f"  ❌ ERROR: {e}")

    print(f"\n{'='*80}")
    print("DIAGNOSIS COMPLETE")
    print(f"{'='*80}\n")


if __name__ == "__main__":
    diagnose_results_files()

